package EjercicioPropuesto40;

import java.text.DecimalFormat;

public class MetodosP40 {
	public static double raiz(double numero) {
		double r;
		r = Math.sqrt(numero);
		return r;
	}

	public static double potencia2(double numero) {
		double p2;
		p2 = Math.pow(numero, 2);
		return p2;
	}

	public static double potencia3(double numero) {
		double p3;
		p3 = Math.pow(numero, 3);
		return p3;
	}
}
